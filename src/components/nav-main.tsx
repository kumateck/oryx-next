interface Props {
  title: string;
  items: MenuItem[];
  currentPath?: string;
}

export function NavMain({ items, title, currentPath }: Props) {
  return (
    <SidebarGroup>
      <SidebarGroupLabel>{title}</SidebarGroupLabel>
      <SidebarMenu>
        {items.map((item) => {
          const isActive = currentPath === item.url;

          return item.items && item.items.length > 0 ? (
            <Collapsible
              key={item.title}
              asChild
              defaultOpen={item.isActive}
              className="group/collapsible"
            >
              <SidebarMenuItem>
                <CollapsibleTrigger asChild>
                  <SidebarMenuButton tooltip={item.title}>
                    {item.icon && <Icon name={item.icon} />}
                    <span>{item.title}</span>
                    <ChevronRight className="ml-auto transition-transform duration-200 group-data-[state=open]/collapsible:rotate-90" />
                  </SidebarMenuButton>
                </CollapsibleTrigger>
                <CollapsibleContent>
                  <SidebarMenuSub>
                    {item.items.map((subItem) => {
                      if (subItem.children) {
                        return (
                          <DropdownMenu key={subItem.title}>
                            <SidebarMenuItem>
                              <DropdownMenuTrigger asChild>
                                <SidebarMenuButton className="data-[state=open]:bg-sidebar-accent data-[state=open]:text-sidebar-accent-foreground">
                                  {subItem.title}{" "}
                                  <MoreHorizontal className="ml-auto" />
                                </SidebarMenuButton>
                              </DropdownMenuTrigger>
                              {subItem.children && subItem.children?.length ? (
                                <DropdownMenuContent
                                  side="right"
                                  align="start"
                                  className="min-w-56 rounded-lg"
                                >
                                  {subItem.children.map((child, idx) => (
                                    <DropdownMenuItem asChild key={idx}>
                                      <Link href={child.url as string}>
                                        {child.title}
                                      </Link>
                                    </DropdownMenuItem>
                                  ))}
                                </DropdownMenuContent>
                              ) : null}
                            </SidebarMenuItem>
                          </DropdownMenu>
                        );
                      } else {
                        return (
                          <SidebarMenuSubButton asChild key={subItem.title}>
                            <Link href={subItem.url as string}>
                              <span>{subItem.title}</span>
                            </Link>
                          </SidebarMenuSubButton>
                        );
                      }
                    })}
                  </SidebarMenuSub>
                </CollapsibleContent>
              </SidebarMenuItem>
            </Collapsible>
          ) : (
            <SidebarMenuItem key={item.title}>
              <SidebarMenuButton
                tooltip={item.title}
                asChild
                isActive={isActive}
              >
                <Link
                  href={item.url as string}
                  className="flex w-full items-center"
                >
                  {item.icon && <Icon name={item.icon} />}
                  <span>{item.title}</span>
                </Link>
              </SidebarMenuButton>
            </SidebarMenuItem>
          );
        })}
      </SidebarMenu>
    </SidebarGroup>
  );
}
